def somar(a,b):
    return f'Resultado da soma entre {a} + {b} = {a + b}'

def subtrair(a,b):
    return f'Resultado da subtração entre {a} - {b} = {a-b}'

def multiplicar(a,b):
    return f'Resultada da multiplicação entre {a} x {b} = {a*b}'

def dividir(a,b):
    return f'Resultado da divisão entre {a} e {b} = {a/b}'

